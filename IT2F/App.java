public class App {
    public static void main(String[] args) {
        String color = "red";

        if (color == "red") {
            System.out.println("STAPH");
        } else if (color == "green") {
            System.out.println("GOOOOOOOOO");
        } else if (color == "Yellow") {
            System.out.println("Get ready to stop!!!");
        } else {
            System.out.println("Invalid Color");
        }
    }
}
